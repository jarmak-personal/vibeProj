"""SVD-compressed datum corrections for cross-datum transforms.

After the existing Helmert datum shift (or in place of it, for grid-only
datum pairs like NAD27→NAD83), we add an additive correction derived from
SVD compression of public-domain grid data.

Math:
    correction(lat, lon) = sum_k S[k] * lerp(U_k, lat_idx) * lerp(V_k, lon_idx)

Corrections are stored in arcseconds and converted to degrees at evaluation
time.  The evaluation function works with both NumPy and CuPy arrays.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import TYPE_CHECKING, Any

if TYPE_CHECKING:
    import types


@dataclass(frozen=True, slots=True)
class DatumCorrectionData:
    """SVD-compressed datum correction coefficients.

    Stores the truncated SVD decomposition of a lat/lon correction grid.
    Each component (lat correction, lon correction) has its own S, U, V^T.

    Coordinates are geographic (degrees), corrections are in arcseconds.
    """

    src_crs: str  # e.g. "EPSG:4267"
    dst_crs: str  # e.g. "EPSG:4269"
    bbox: tuple[float, float, float, float]  # (lat_min, lat_max, lon_min, lon_max)
    n_lat: int  # grid rows
    n_lon: int  # grid columns
    rank: int  # SVD truncation rank

    # Lat correction: correction_lat = sum_k s_lat[k] * U_lat[k,:] * Vt_lat[k,:]
    s_lat: tuple[float, ...]  # singular values (rank,)
    u_lat: tuple[tuple[float, ...], ...]  # U vectors (rank x n_lat)
    vt_lat: tuple[tuple[float, ...], ...]  # V^T vectors (rank x n_lon)

    # Lon correction: correction_lon = sum_k s_lon[k] * U_lon[k,:] * Vt_lon[k,:]
    s_lon: tuple[float, ...]  # singular values (rank,)
    u_lon: tuple[tuple[float, ...], ...]  # U vectors (rank x n_lat)
    vt_lon: tuple[tuple[float, ...], ...]  # V^T vectors (rank x n_lon)

    source: str  # attribution string
    has_helmert: bool  # True = residual after Helmert, False = full correction


# ---------------------------------------------------------------------------
# Registry
# ---------------------------------------------------------------------------

_CORRECTIONS: dict[tuple[str, str], DatumCorrectionData] = {}


def _register(correction: DatumCorrectionData) -> None:
    """Register a datum correction in the lookup table."""
    _CORRECTIONS[(correction.src_crs, correction.dst_crs)] = correction


def get_datum_correction(src_crs: str, dst_crs: str) -> DatumCorrectionData | None:
    """Look up an SVD datum correction for a CRS pair.

    Checks both (src, dst) and (dst, src) directions.  When the reverse
    direction is found, the caller should use ``negate=True`` during
    evaluation.

    Parameters
    ----------
    src_crs, dst_crs : str
        Authority codes, e.g. "EPSG:4267".

    Returns
    -------
    DatumCorrectionData or None
        None if no correction is available for this pair.
    """
    result = _CORRECTIONS.get((src_crs, dst_crs))
    if result is not None:
        return result
    return _CORRECTIONS.get((dst_crs, src_crs))


def is_reverse_direction(src_crs: str, dst_crs: str) -> bool:
    """Return True if the correction is stored in the reverse direction.

    When True, the caller should apply with ``negate=True``.
    """
    if (src_crs, dst_crs) in _CORRECTIONS:
        return False
    if (dst_crs, src_crs) in _CORRECTIONS:
        return True
    return False


# ---------------------------------------------------------------------------
# xp evaluation
# ---------------------------------------------------------------------------


def apply_svd_correction(
    lat: Any,
    lon: Any,
    correction: DatumCorrectionData,
    xp: types.ModuleType,
    *,
    negate: bool = False,
    out_lat: Any | None = None,
    out_lon: Any | None = None,
) -> tuple[Any, Any]:
    """Apply SVD-compressed datum correction to lat/lon arrays.

    Reconstructs the correction at the 4 nearest grid nodes using the
    truncated SVD, then bilinearly interpolates.  This is mathematically
    equivalent to bilinear interpolation of the full reconstructed grid
    but avoids materializing the grid.

    Parameters
    ----------
    lat, lon : array_like
        Geographic coordinates in degrees.
    correction : DatumCorrectionData
        SVD coefficients.
    xp : module
        Array module (numpy or cupy).
    negate : bool
        If True, subtract the correction (for inverse direction).
    out_lat, out_lon : array_like, optional
        Pre-allocated output arrays (zero-copy support).

    Returns
    -------
    (lat_out, lon_out) : tuple of arrays
        Corrected geographic coordinates in degrees.
    """
    lat_min, lat_max, lon_min, lon_max = correction.bbox
    n_lat = correction.n_lat
    n_lon = correction.n_lon
    rank = correction.rank

    # Normalize to fractional grid indices
    lat_idx = (lat - lat_min) / (lat_max - lat_min) * (n_lat - 1)
    lon_idx = (lon - lon_min) / (lon_max - lon_min) * (n_lon - 1)

    # Clamp to valid grid range
    lat_idx = xp.clip(lat_idx, 0.0, n_lat - 1.0)
    lon_idx = xp.clip(lon_idx, 0.0, n_lon - 1.0)

    # Integer indices for the 4 corners of the bilinear cell
    lat_i0 = xp.floor(lat_idx).astype(int)
    lon_i0 = xp.floor(lon_idx).astype(int)
    lat_i1 = xp.minimum(lat_i0 + 1, n_lat - 1)
    lon_i1 = xp.minimum(lon_i0 + 1, n_lon - 1)

    lat_frac = lat_idx - lat_i0
    lon_frac = lon_idx - lon_i0

    # Reconstruct SVD correction at 4 corner grid nodes, then bilinear interpolate.
    # For each SVD component k, the grid value at (i, j) is:
    #   S[k] * U[k, i] * Vt[k, j]
    # Sum over k gives the full correction at (i, j).
    # Bilinear interpolation of these 4 reconstructed values gives the output.

    dlat_00 = xp.zeros_like(lat, dtype=xp.float64)
    dlat_01 = xp.zeros_like(lat, dtype=xp.float64)
    dlat_10 = xp.zeros_like(lat, dtype=xp.float64)
    dlat_11 = xp.zeros_like(lat, dtype=xp.float64)

    dlon_00 = xp.zeros_like(lon, dtype=xp.float64)
    dlon_01 = xp.zeros_like(lon, dtype=xp.float64)
    dlon_10 = xp.zeros_like(lon, dtype=xp.float64)
    dlon_11 = xp.zeros_like(lon, dtype=xp.float64)

    for k in range(rank):
        # Lat correction: reconstruct at 4 corners
        u_lat_k = xp.asarray(correction.u_lat[k], dtype=xp.float64)
        vt_lat_k = xp.asarray(correction.vt_lat[k], dtype=xp.float64)
        s_lat_k = correction.s_lat[k]

        u0 = u_lat_k[lat_i0]
        u1 = u_lat_k[lat_i1]
        v0 = vt_lat_k[lon_i0]
        v1 = vt_lat_k[lon_i1]

        dlat_00 += s_lat_k * u0 * v0
        dlat_01 += s_lat_k * u0 * v1
        dlat_10 += s_lat_k * u1 * v0
        dlat_11 += s_lat_k * u1 * v1

        # Lon correction: reconstruct at 4 corners
        u_lon_k = xp.asarray(correction.u_lon[k], dtype=xp.float64)
        vt_lon_k = xp.asarray(correction.vt_lon[k], dtype=xp.float64)
        s_lon_k = correction.s_lon[k]

        u0 = u_lon_k[lat_i0]
        u1 = u_lon_k[lat_i1]
        v0 = vt_lon_k[lon_i0]
        v1 = vt_lon_k[lon_i1]

        dlon_00 += s_lon_k * u0 * v0
        dlon_01 += s_lon_k * u0 * v1
        dlon_10 += s_lon_k * u1 * v0
        dlon_11 += s_lon_k * u1 * v1

    # Bilinear interpolation of the 4 corner values
    w00 = (1.0 - lat_frac) * (1.0 - lon_frac)
    w01 = (1.0 - lat_frac) * lon_frac
    w10 = lat_frac * (1.0 - lon_frac)
    w11 = lat_frac * lon_frac

    dlat = w00 * dlat_00 + w01 * dlat_01 + w10 * dlat_10 + w11 * dlat_11
    dlon = w00 * dlon_00 + w01 * dlon_01 + w10 * dlon_10 + w11 * dlon_11

    # Convert arcseconds to degrees
    dlat_deg = dlat / 3600.0
    dlon_deg = dlon / 3600.0

    # Apply correction
    if negate:
        lat_out = lat - dlat_deg
        lon_out = lon - dlon_deg
    else:
        lat_out = lat + dlat_deg
        lon_out = lon + dlon_deg

    # Write into pre-allocated output buffers when provided
    if out_lat is not None:
        out_lat[:] = lat_out  # type: ignore[index]
        lat_out = out_lat
    if out_lon is not None:
        out_lon[:] = lon_out  # type: ignore[index]
        lon_out = out_lon

    return lat_out, lon_out


